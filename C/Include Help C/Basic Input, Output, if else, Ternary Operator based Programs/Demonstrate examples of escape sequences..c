/*C program to demonstrate examples of escape sequences.*/
 
#include <stdio.h>
int main()
{
     
    printf("Hello\nWorld!");    //use of \n
 
    printf("\nHello\tWorld!");  // use of \t
 
    printf("\n\"Hello World!\"");   //use of \"
 
    printf("\nHello\bWorld!");      //use of \b
	getch();
    return 0;
}
